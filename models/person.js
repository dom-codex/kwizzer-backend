const Sequelize = require("sequelize");
const sequelize = require("../utils/database");

const Person = sequelize.define("person", {
  id: {
    type: Sequelize.INTEGER,
    autoIncrement: true,
    allowNull: false,
    primaryKey: true,
  },
  name: {
    type: Sequelize.STRING,
    allowNull: false,
  },
  password: {
    type: Sequelize.STRING,
    allowNull: false,
  },
  phone: {
    type: Sequelize.STRING,
    allowNull: false,
  },
  email: {
    type: Sequelize.STRING,
    allowNull: false,
  },
  ref: {
    type: Sequelize.STRING,
    allowNull: false,
  },
});
module.exports = Person;
